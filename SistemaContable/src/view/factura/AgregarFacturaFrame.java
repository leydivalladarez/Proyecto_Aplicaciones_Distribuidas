    private void cargarClientes(){
        try {
            cbxCliente.setModel(modeloCliente);
            List<Cliente> clientes = clienteService.obtenerTodosLosClientes();
            for (Cliente cliente : clientes) {
                modeloCliente.addElement(cliente);
            }
        } catch (Exception e) {
            Logger.getLogger(AgregarFacturaFrame.class.getName()).log(Level.SEVERE, null, e);
        }        
    }
    
    private void cargarCiudades(){
        try {
            cbxCiudad.setModel(modeloCiudad);
            List<Ciudad> ciudades = ciudadService.obtenerTodasLasCiudades();
            for (Ciudad ciudad : ciudades) {
                modeloCiudad.addElement(ciudad);
            }
        } catch (Exception e) {
            Logger.getLogger(AgregarFacturaFrame.class.getName()).log(Level.SEVERE, null, e);
        }        
    }

